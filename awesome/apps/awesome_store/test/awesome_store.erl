-module(awesome_store).
-compile(export_all).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
create_table(Store, Opts) ->
    mnesia:create_table(Store, Opts).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
read(Store, Key) ->
    mnesia:dirty(Store, Key).

%%--------------------------------------------------------------------
%%
%%--------------------------------------------------------------------
write(Store, Key, Value) ->
    mnesia:dirty_write({?MODULE, Key, Value}).

    
